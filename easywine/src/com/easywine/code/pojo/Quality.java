package com.easywine.code.pojo;

public class Quality {
	private Integer qualityID;
	private Integer qualityCode;
	private String qualityName;
	private String qualityEName;
	public Integer getQualityID() {
		return qualityID;
	}
	public void setQualityID(Integer qualityID) {
		this.qualityID = qualityID;
	}
	public Integer getQualityCode() {
		return qualityCode;
	}
	public void setQualityCode(Integer qualityCode) {
		this.qualityCode = qualityCode;
	}
	public String getQualityName() {
		return qualityName;
	}
	public void setQualityName(String qualityName) {
		this.qualityName = qualityName;
	}
	public String getQualityEName() {
		return qualityEName;
	}
	public void setQualityEName(String qualityEName) {
		this.qualityEName = qualityEName;
	}
}
