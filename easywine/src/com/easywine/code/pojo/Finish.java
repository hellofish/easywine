package com.easywine.code.pojo;

public class Finish {
	private Integer finishID;
	private Integer finishCode;
	private String finishName;
	private String finishEName;
	public Integer getFinishID() {
		return finishID;
	}
	public void setFinishID(Integer finishID) {
		this.finishID = finishID;
	}
	public Integer getFinishCode() {
		return finishCode;
	}
	public void setFinishCode(Integer finishCode) {
		this.finishCode = finishCode;
	}
	public String getFinishName() {
		return finishName;
	}
	public void setFinishName(String finishName) {
		this.finishName = finishName;
	}
	public String getFinishEName() {
		return finishEName;
	}
	public void setFinishEName(String finishEName) {
		this.finishEName = finishEName;
	}
}
