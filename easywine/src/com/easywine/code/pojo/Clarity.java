package com.easywine.code.pojo;

public class Clarity {
	private Integer clarityID;
	private Integer clarityCode;
	private String clarityName;
	private String clarityEName;
	public Integer getClarityID() {
		return clarityID;
	}
	public void setClarityID(Integer clarityID) {
		this.clarityID = clarityID;
	}
	public Integer getClarityCode() {
		return clarityCode;
	}
	public void setClarityCode(Integer clarityCode) {
		this.clarityCode = clarityCode;
	}
	public String getClarityName() {
		return clarityName;
	}
	public void setClarityName(String clarityName) {
		this.clarityName = clarityName;
	}
	public String getClarityEName() {
		return clarityEName;
	}
	public void setClarityEName(String clarityEName) {
		this.clarityEName = clarityEName;
	}
}
