package com.easywine.code.pojo;

public class Body {
	private Integer bodyID;
	private Integer bodyCode;
	private String bodyName;
	private String bodyEName;
	public Integer getBodyID() {
		return bodyID;
	}
	public void setBodyID(Integer bodyID) {
		this.bodyID = bodyID;
	}
	public Integer getBodyCode() {
		return bodyCode;
	}
	public void setBodyCode(Integer bodyCode) {
		this.bodyCode = bodyCode;
	}
	public String getBodyName() {
		return bodyName;
	}
	public void setBodyName(String bodyName) {
		this.bodyName = bodyName;
	}
	public String getBodyEName() {
		return bodyEName;
	}
	public void setBodyEName(String bodyEName) {
		this.bodyEName = bodyEName;
	}
}
