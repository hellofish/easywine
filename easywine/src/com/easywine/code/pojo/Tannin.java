package com.easywine.code.pojo;

public class Tannin {
	private Integer tanninID;
	private Integer tanninCode;
	private String tanninName;
	private String tanninEName;
	public Integer getTanninID() {
		return tanninID;
	}
	public void setTanninID(Integer tanninID) {
		this.tanninID = tanninID;
	}
	public Integer getTanninCode() {
		return tanninCode;
	}
	public void setTanninCode(Integer tanninCode) {
		this.tanninCode = tanninCode;
	}
	public String getTanninName() {
		return tanninName;
	}
	public void setTanninName(String tanninName) {
		this.tanninName = tanninName;
	}
	public String getTanninEName() {
		return tanninEName;
	}
	public void setTanninEName(String tanninEName) {
		this.tanninEName = tanninEName;
	}
}
