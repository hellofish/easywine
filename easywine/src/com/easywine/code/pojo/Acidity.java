package com.easywine.code.pojo;

public class Acidity {
	private Integer acidityID;
	private Integer acidityCode;
	private String acidityName;
	private String acidityEName;
	public Integer getAcidityID() {
		return acidityID;
	}
	public void setAcidityID(Integer acidityID) {
		this.acidityID = acidityID;
	}
	public Integer getAcidityCode() {
		return acidityCode;
	}
	public void setAcidityCode(Integer acidityCode) {
		this.acidityCode = acidityCode;
	}
	public String getAcidityName() {
		return acidityName;
	}
	public void setAcidityName(String acidityName) {
		this.acidityName = acidityName;
	}
	public String getAcidityEName() {
		return acidityEName;
	}
	public void setAcidityEName(String acidityEName) {
		this.acidityEName = acidityEName;
	}
	
}
