package com.easywine.code.pojo;

public class Condition {
	private Integer conditionID;
	private Integer conditionCode;
	private String conditionName;
	private String conditionEName;
	public Integer getConditionID() {
		return conditionID;
	}
	public void setConditionID(Integer conditionID) {
		this.conditionID = conditionID;
	}
	public Integer getConditionCode() {
		return conditionCode;
	}
	public void setConditionCode(Integer conditionCode) {
		this.conditionCode = conditionCode;
	}
	public String getConditionName() {
		return conditionName;
	}
	public void setConditionName(String conditionName) {
		this.conditionName = conditionName;
	}
	public String getConditionEName() {
		return conditionEName;
	}
	public void setConditionEName(String conditionEName) {
		this.conditionEName = conditionEName;
	}
	
}
