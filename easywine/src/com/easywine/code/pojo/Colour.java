package com.easywine.code.pojo;

public class Colour {
	private Integer colourID;
	private Integer colourCode;
	private Integer colourType;
	private String colourName;
	private String colourEName;
	public Integer getColourID() {
		return colourID;
	}
	public void setColourID(Integer colourID) {
		this.colourID = colourID;
	}
	public Integer getColourCode() {
		return colourCode;
	}
	public void setColourCode(Integer colourCode) {
		this.colourCode = colourCode;
	}
	public Integer getColourType() {
		return colourType;
	}
	public void setColourType(Integer colourType) {
		this.colourType = colourType;
	}
	public String getColourName() {
		return colourName;
	}
	public void setColourName(String colourName) {
		this.colourName = colourName;
	}
	public String getColourEName() {
		return colourEName;
	}
	public void setColourEName(String colourEName) {
		this.colourEName = colourEName;
	}
}
